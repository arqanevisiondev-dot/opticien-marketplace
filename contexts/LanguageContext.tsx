type Language = "fr" | "en" | "ar"
type Translations = (typeof translations)["fr"]

interface LanguageContextType {
  language: Language
  setLanguage: (lang: Language) => void
  t: Translations
}

const LanguageContext = createContext<LanguageContextType | undefined>(undefined)

export function LanguageProvider({ children }: { children: ReactNode }) {
  const [language, setLanguage] = useState<Language>("fr")

  // On mount, try to load saved language from localStorage
  useEffect(() => {
    try {
      const saved = localStorage.getItem('language')
      if (saved === 'fr' || saved === 'en' || saved === 'ar') {
        setLanguage(saved as Language)
        return
      }
    } catch (e) {
      // ignore localStorage errors
    }
  }, [])

  // wrapper to persist language selection to localStorage
  const persistLanguage = (lang: Language) => {
    setLanguage(lang)
    try {
      localStorage.setItem('language', lang)
    } catch (e) {
      // ignore localStorage write errors
    }
  }

  const value: LanguageContextType = {
    language,
    setLanguage: persistLanguage,
    t: translations[language],
  }

  return <LanguageContext.Provider value={value}>{children}</LanguageContext.Provider>
}

export function useLanguage(): LanguageContextType {
  const context = useContext(LanguageContext)
  if (context === undefined) {
    throw new Error("useLanguage must be used within a LanguageProvider")
  }
  return context
}
